namespace LearnCSharp
{
    public class NumericTypes
    {
        public static void Run()
        {
            // Declare a variable of type int with a value of 42
            int myInt = 42;
            Console.WriteLine(myInt);

            // Declare a variable of type double with a value of 42.0
            double myDouble = 42.0;
            Console.WriteLine(myDouble);

            // Declare a variable of type decimal with a value of 42.0
            decimal myDecimal = 42.0m;
            Console.WriteLine(myDecimal);

            // Declare a variable of type long with a value of 42
            long myLong = 42;
            Console.WriteLine(myLong);

            // Declare a variable of type float with a value of 42.0
            float myFloat = 42.0f;
            Console.WriteLine(myFloat);

            // Declare a variable of type short with a value of 42
            short myShort = 42;
            Console.WriteLine(myShort);

            // Declare a variable of type byte with a value of 42
            byte myByte = 42;
            Console.WriteLine(myByte);

            // Declare a variable of type int with a value of 42
            // and then assign a value of 43
            int myInt2 = 42;
            myInt2 = 43;
            Console.WriteLine(myInt2);

            // Declare a variable of type double with a value of 42.0
            // and then assign a value of 43.0
            double myDouble2 = 42.0;
            myDouble2 = 43.0;
            Console.WriteLine(myDouble2);

            // Declare a variable of type decimal with a value of 42.0
            // and then assign a value of 43.0
            decimal myDecimal2 = 42.0m;
            myDecimal2 = 43.0m;
            Console.WriteLine(myDecimal2);

            // Declare a variable of type long with a value of 42
            // and then assign a value of 43
            long myLong2 = 42;
            myLong2 = 43;
            Console.WriteLine(myLong2);

            // Declare a variable of type float with a value of 42.0
            // and then assign a value of 43.0
            float myFloat2 = 42.0f;
            myFloat2 = 43.0f;
            Console.WriteLine(myFloat2);
        }
    }
}